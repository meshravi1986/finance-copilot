def format_indian_currency_rupee(value):

    value = float(value)

    #################################################
    # CRORES
    #################################################

    if value >= 10000000:

        return (
            f"₹{round(value / 10000000, 2)} Cr"
        )

    #################################################
    # LAKHS
    #################################################

    elif value >= 100000:

        return (
            f"₹{round(value / 100000, 2)} L"
        )

    #################################################
    # THOUSANDS
    #################################################

    elif value >= 1000:

        return (
            f"₹{round(value / 1000, 2)} K"
        )

    #################################################
    # NORMAL
    #################################################

    else:

        return f"₹{round(value, 2)}"
